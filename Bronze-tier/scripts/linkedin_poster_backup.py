    log("Checking for approved LinkedIn posts...")
    results = process_approved_posts()

    if results:
        print()
        print("=" * 60)
        print("POSTING SUMMARY")
        print("=" * 60)
        for i, result in enumerate(results, 1):
            print(f"{i}. {result}")

        # Circuit breaker status
        cb_state = circuit_breaker.get_state()
        print(f"Circuit State:   {cb_state['state']}")

        # Retry queue status
        queue_stats = queue_manager.get_queue_stats()
        if queue_stats['total_tasks'] > 0:
            print(f"Retry Queue:     {queue_stats['total_tasks']} tasks pending")

        print("=" * 60)
    else:
        # Generate a sample post for demonstration
        log("No approved posts. Generating sample post for approval...")
        sample = generate_business_post()
        create_approval_request(sample["content"], sample["hashtags"])
        print()
        log("Sample post created. Move it to /Approved to post.")

    # Audit log: system stopped
    audit_logger.log(
        action_type=ActionType.SYSTEM_STOPPED,
        actor=ACTOR,
        target="linkedin_poster",
        parameters={
            'posts_processed': len(results),
            'successful': sum(1 for r in results if r.get('success', False))
        },
        result=ResultStatus.SUCCESS
    )
    audit_logger.flush()


