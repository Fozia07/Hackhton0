def log(message, level="INFO"):
    """Print a timestamped log message."""
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    print(f"[{timestamp}] [{level}] {message}")


def log_to_file(action, details):
    """Log action to daily log file."""
    LOGS_DIR.mkdir(parents=True, exist_ok=True)
    log_file = LOGS_DIR / f"{datetime.now().strftime('%Y-%m-%d')}_linkedin.json"

    entry = {
        "timestamp": datetime.now().isoformat(),
        "action": action,
        "details": details
    }

    logs = []
    if log_file.exists():
        try:
            with open(log_file, "r") as f:
                logs = json.load(f)
        except:
            logs = []

    logs.append(entry)

    with open(log_file, "w") as f:
        json.dump(logs, f, indent=2)


def create_approval_request(content, hashtags=None, schedule_time=None):
    """Create an approval request for a LinkedIn post."""
    start_time = datetime.now()
    PENDING_DIR.mkdir(parents=True, exist_ok=True)

    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    filename = f"LINKEDIN_POST_{timestamp}.md"
    filepath = PENDING_DIR / filename

    hashtags_str = " ".join([f"#{h}" for h in (hashtags or [])])
    schedule_info = schedule_time or "Immediate (upon approval)"

    request_content = f"""---
type: approval_request
request_id: LINKEDIN_{timestamp}
action: post_linkedin
created: {datetime.now().isoformat()}
schedule_time: {schedule_info}
status: pending
priority: medium
---

# LinkedIn Post Approval Request

## Post Content

{content}

## Hashtags

{hashtags_str or "No hashtags specified"}

## Schedule

**Post Time:** {schedule_info}

## Preview

---
{content}

{hashtags_str}
---

## Instructions

**To Approve:** Move this file to `/Approved/` folder

**To Reject:** Move this file to `/Rejected/` folder

---
*Generated by LinkedIn Poster Skill*
*Awaiting human approval*
"""

    with open(filepath, "w") as f:
        f.write(request_content)

    log(f"Created approval request: {filename}")

    # Audit log: approval requested
    audit_logger.log_with_duration(
        action_type=ActionType.APPROVAL_REQUESTED,
        actor=ACTOR,
        target=str(filepath),
        start_time=start_time,
        parameters={
            'content_length': len(content),
            'hashtags': hashtags or [],
            'schedule_time': schedule_info
        },
        approval_status=ApprovalStatus.PENDING,
        result=ResultStatus.SUCCESS
    )

    return filepath


def post_to_linkedin_simulated(content, hashtags=None):
    """Simulate posting to LinkedIn."""
    start_time = datetime.now()
    log("SIMULATION: Would post to LinkedIn")
    log(f"  Content: {content[:50]}...")
